package com.indivaragroup.operator;

public class ComparisonOperator {
    public void runDemonstration () {

        int a = 5;
        int b = 11;

        System.out.println(a>b);  // false
        System.out.println(a<b);  // true
        System.out.println(a>=b); // false
        System.out.println(a<=b); // true
        System.out.println(a==b); // false
        System.out.println(a!=b); // true
    }
}